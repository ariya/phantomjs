        push(@headerContent, "    {\n");
        push(@headerContent, "        if (m_impl) {\n");
        push(@headerContent, "            m_impl->deref();\n");
        push(@headerContent, "            m_impl = 0;\n");
        push(@headerContent, "        }\n");
        push(@headerContent, "    }\n\n");
        push(@headerContent, "private:\n");
        push(@headerContent, "    $implType* m_impl;\n");
