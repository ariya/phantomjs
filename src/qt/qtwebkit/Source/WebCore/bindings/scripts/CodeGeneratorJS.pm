sub SkipIncludeHeader
{
    my $type = shift;

    return 1 if $codeGenerator->SkipIncludeHeader($type);
    return 1 if $codeGenerator->GetSequenceType($type) or $codeGenerator->GetArrayType($type);
    return $typesWithoutHeader{$type};
